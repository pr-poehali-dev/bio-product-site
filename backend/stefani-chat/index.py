def try_openrouter(api_messages: list) -> str | None:
    """OpenRouter — бесплатные модели (Llama, Mistral, Qwen)."""
    api_key = os.environ.get("OPENROUTER_API_KEY", "")
    if not api_key:
        return None

    payload = json.dumps({
        "model": "meta-llama/llama-3.1-8b-instruct:free",
        "messages": api_messages,
        "max_tokens": 800,
        "temperature": 0.9,
    }).encode("utf-8")

    req = urllib.request.Request(
        "https://openrouter.ai/api/v1/chat/completions",
        data=payload,
        headers={
            "Content-Type": "application/json",
            "Authorization": f"Bearer {api_key}",
            "HTTP-Referer": "https://stefani.ai",
            "X-Title": "Stefani AI",
        },
        method="POST",
    )
    try:
        with urllib.request.urlopen(req, timeout=25) as resp:
            result = json.loads(resp.read().decode("utf-8"))
            return result["choices"][0]["message"]["content"]
    except Exception:
        return None


def try_groq(api_messages: list) -> str | None:
    """Groq — если есть ключ."""
    api_key = os.environ.get("GROQ_API_KEY", "")
    if not api_key:
        return None

    payload = json.dumps({
        "model": "llama-3.1-8b-instant",
        "messages": api_messages,
        "max_tokens": 800,
        "temperature": 0.9,
    }).encode("utf-8")

    req = urllib.request.Request(
        "https://api.groq.com/openai/v1/chat/completions",
        data=payload,
        headers={
            "Content-Type": "application/json",
            "Authorization": f"Bearer {api_key}",
        },
        method="POST",
    )
    try:
        with urllib.request.urlopen(req, timeout=20) as resp:
            result = json.loads(resp.read().decode("utf-8"))
            return result["choices"][0]["message"]["content"]
    except Exception:
        return None


def try_deepinfra(api_messages: list) -> str | None:
    """DeepInfra публичный endpoint."""
    payload = json.dumps({
        "model": "meta-llama/Meta-Llama-3-8B-Instruct",
        "messages": api_messages,
        "max_new_tokens": 600,
        "temperature": 0.9,
    }).encode("utf-8")

    req = urllib.request.Request(
        "https://api.deepinfra.com/v1/openai/chat/completions",
        data=payload,
        headers={
            "Content-Type": "application/json",
            "User-Agent": "Mozilla/5.0 (Windows NT 10.0; Win64; x64)",
            "Referer": "https://deepinfra.com",
            "Origin": "https://deepinfra.com",
            "X-Deepinfra-Source": "web-embed",
        },
        method="POST",
    )
    try:
        with urllib.request.urlopen(req, timeout=18) as resp:
            result = json.loads(resp.read().decode("utf-8"))
            text = result["choices"][0]["message"]["content"]
            return text if text and len(text) > 5 else None
    except Exception:
        return None


def detect_emotion(text: str, mood: str) -> str:
    t = text.lower()
    if mood == "intense": return "intense"
    if mood == "playful": return "playful"
    if any(w in t for w in ["рада", "здорово", "отлично", "люблю", "классно", "кайф", "!"]):
        return "happy"
    if any(w in t for w in ["понимаю", "жаль", "сложно", "грустно"]):
        return "empathetic"
    if any(w in t for w in ["осторожно", "важно", "серьёзно", "опасно"]):
        return "serious"
    if any(w in t for w in ["интересно", "хм", "любопытно", "удивительно"]):
        return "curious"
    if any(w in t for w in ["```", "def ", "import ", "class ", "код"]):
        return "focused"
    if mood == "calm": return "calm"
    return "neutral"


