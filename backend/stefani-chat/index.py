def smart_fallback(user_message: str, mood: str, user_name: str | None, history: list) -> str:
    msg_lower = user_message.lower().strip()

    # Проверяем базу знаний
    for keywords, responses in KNOWLEDGE_BASE.items():
        if any(kw in msg_lower for kw in keywords):
            reply = random.choice(responses)
            # Добавляем имя если есть
            if user_name and random.random() > 0.6 and not user_name.lower() in reply.lower():
                reply = f"{user_name}, {reply[0].lower()}{reply[1:]}"
            return reply

    # Пробуем понять тему и дать хоть что-то
    # Вопросы о знаменитостях
    if any(w in msg_lower for w in ["кто такой", "кто такая", "кто это", "расскажи про", "кем был", "кем является"]):
        name_part = msg_lower.replace("кто такой", "").replace("кто такая", "").replace("кто это", "").replace("расскажи про", "").replace("кем был", "").strip()
        if name_part:
            return f"Про {name_part} — интересная личность. К сожалению прямо сейчас не могу вытащить точные данные из памяти, но если это публичный человек, учёный или историческая фигура — скажи в какой сфере, и я расскажу что знаю."

    # Как что-то сделать
    if any(w in msg_lower for w in ["как", "каким образом", "каким способом", "что нужно чтобы", "как можно"]):
        return f"Конкретный ответ зависит от деталей. Расскажи подробнее — что именно хочешь сделать, какие условия, что уже пробовал? Тогда дам конкретные шаги."

    # Что такое X
    if any(w in msg_lower for w in ["что такое", "что это", "объясни", "расскажи что"]):
        topic = msg_lower.replace("что такое", "").replace("что это", "").replace("объясни", "").replace("расскажи что", "").strip()
        if topic:
            return f"'{topic.capitalize()}' — дай секунду сформулировать. Это тема которую можно объяснить по-разному. Тебе нужно общее понимание или хочешь углубиться в детали?"

    # Если просто неопределённое сообщение
    if len(msg_lower) < 15:
        short_responses = [
            "Давай. Рассказывай.",
            "Слушаю.",
            "Ага, и что дальше?",
            "Понятно. Продолжай.",
            "Хм, интересно. Что ты имеешь в виду?",
        ]
        return random.choice(short_responses)

    return random.choice(UNKNOWN_RESPONSES)


def handler(event: dict, context) -> dict:
    """Обработчик чата Stefani."""

    if event.get("httpMethod") == "OPTIONS":
        return {"statusCode": 200, "headers": CORS_HEADERS, "body": ""}

    try:
        body = json.loads(event.get("body") or "{}")
    except Exception:
        return {
            "statusCode": 400,
            "headers": {**CORS_HEADERS, "Content-Type": "application/json"},
            "body": json.dumps({"error": "INVALID_JSON"}),
        }

    messages = body.get("messages", [])
    mood = body.get("mood", "calm")
    user_name = (body.get("user_name") or "").strip() or None

    system_content = build_system_prompt(mood, user_name)

    api_messages = [{"role": "system", "content": system_content}]
    for msg in messages[-20:]:
        role = "user" if msg.get("role") == "user" else "assistant"
        text = msg.get("text", "").strip()
        if text:
            api_messages.append({"role": role, "content": text})

    last_user_text = next(
        (m.get("text", "") for m in reversed(messages) if m.get("role") == "user"), ""
    )

    # Пробуем провайдеры
    reply = try_groq(api_messages)
    model_used = "llama-3.1 (groq)"

    if not reply:
        reply = try_openrouter(api_messages)
        model_used = "llama-3.1 (openrouter)"

    if not reply:
        reply = try_deepinfra(api_messages)
        model_used = "llama-3 (deepinfra)"

    if not reply:
        reply = smart_fallback(last_user_text, mood, user_name, messages)
        model_used = "stefani-local"

    emotion = detect_emotion(reply, mood)

    return {
        "statusCode": 200,
        "headers": {**CORS_HEADERS, "Content-Type": "application/json"},
        "body": json.dumps({"reply": reply, "model": model_used, "emotion": emotion}, ensure_ascii=False),
    }
